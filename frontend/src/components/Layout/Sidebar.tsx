import { Link, useLocation } from "react-router-dom";

interface SidebarProps {
  isOpen: boolean;
  onClose: () => void;
}

const Sidebar = ({ isOpen, onClose }: SidebarProps) => {
  const location = useLocation();

  const navItems = [
    { path: "/dashboard", label: "Dashboard", icon: DashboardIcon },
    { path: "/dashboard/clients", label: "Clients", icon: ClientsIcon },
    { path: "/dashboard/invoice-list", label: "Invoices", icon: InvoicesIcon },
    { path: "/dashboard/profile", label: "Profile", icon: ProfileIcon },
  ];

  const isActive = (path: string) => {
    if (path === "/dashboard") {
      return location.pathname === "/dashboard";
    }
    return location.pathname.startsWith(path);
  };

  return (
    <aside
      className={`
        fixed top-0 left-0 z-50 h-full w-64 
        bg-[var(--card)] border-r border-[var(--border)]
        transform transition-transform duration-300 ease-in-out
        lg:translate-x-0
        ${isOpen ? "translate-x-0" : "-translate-x-full"}
      `}
    >
      {/* Logo */}
      <div className="flex items-center justify-between h-16 px-6 border-b border-[var(--border)]">
        <Link
          to="/dashboard"
          className="flex items-center gap-2"
          onClick={onClose}
        >
          <div className="w-8 h-8 rounded-lg bg-[var(--primary)] flex items-center justify-center">
            <span className="text-white font-bold text-sm">IV</span>
          </div>
          <span className="text-lg font-semibold text-[var(--foreground)]">
            Invoice Generator
          </span>
        </Link>
        <button
          onClick={onClose}
          className="lg:hidden p-2 rounded-lg hover:bg-[var(--secondary)] text-[var(--muted-foreground)]"
        >
          <CloseIcon />
        </button>
      </div>

      {/* Navigation */}
      <nav className="p-4">
        <ul className="space-y-1">
          {navItems.map((item) => {
            const Icon = item.icon;
            const active = isActive(item.path);
            return (
              <li key={item.path}>
                <Link
                  to={item.path}
                  onClick={onClose}
                  className={`
                    flex items-center gap-3 px-4 py-3 rounded-lg
                    transition-colors duration-200
                    ${
                      active
                        ? "bg-[var(--primary)] text-white"
                        : "text-[var(--muted-foreground)] hover:bg-[var(--secondary)] hover:text-[var(--foreground)]"
                    }
                  `}
                >
                  <Icon className="w-5 h-5" />
                  <span className="font-medium">{item.label}</span>
                </Link>
              </li>
            );
          })}
        </ul>
      </nav>

      {/* Footer */}
      <div className="absolute bottom-0 left-0 right-0 p-4 border-t border-[var(--border)]">
        <div className="text-xs text-[var(--muted-foreground)] text-center">
          Invoice Generator v1.0
        </div>
      </div>
    </aside>
  );
};

